import mongoose, { Schema } from "mongoose";
import { IGrosaryList } from "./grosaryList.interface";

const GrosaryListScheme: Schema = new Schema(
  {
    grosaryName: {
      type: String,
      required: true,
      trim: true,
    },
    status: {
      type: String,
      enum: ["Active", "Inactive"],
      default: "Active",
    },
    isDeleted: {
      type: Boolean,
      default: false,
    },
  },
  {
    timestamps: true,
    toJSON: {
      transform: function (doc, ret: any) {
        ret.createdAt = new Date(ret.createdAt).toLocaleString("en-IN", {
          timeZone: "Asia/Kolkata",
        });
        ret.updatedAt = new Date(ret.updatedAt).toLocaleString("en-IN", {
          timeZone: "Asia/Kolkata",
        });
      },
    },
  },
);

export const GrosaryList = mongoose.model<IGrosaryList>(
  "GrosaryList",
  GrosaryListScheme,
);
